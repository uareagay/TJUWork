import Foundation


struct OutboxListModel: Codable {
    let status: Bool
    let code: Int
    let message: String
    var data: OutboxData
}

struct OutboxData: Codable {
    var data: [OutboxListData]
}

struct OutboxListData: Codable {
    let mid: Int
    let author, type, title: String
    let text: String
    let from: Date
    let to: Date?
    let fraction: String?
}

// MARK: Convenience initializers and mutators

extension OutboxListModel {
    init(data: Data) throws {
        let decoder = JSONDecoder()
        let formatter = DateFormatter()
        formatter.dateFormat = "yyyy-MM-dd HH:mm:ss"
        decoder.dateDecodingStrategy = .formatted(formatter)
        self = try decoder.decode(OutboxListModel.self, from: data)
        //self = try newJSONDecoder().decode(OutboxListModel.self, from: data)
    }
    
    init(_ json: String, using encoding: String.Encoding = .utf8) throws {
        guard let data = json.data(using: encoding) else {
            throw NSError(domain: "JSONDecoding", code: 0, userInfo: nil)
        }
        try self.init(data: data)
    }
    
    init(fromURL url: URL) throws {
        try self.init(data: try Data(contentsOf: url))
    }
    
    func jsonData() throws -> Data {
        return try newJSONEncoder().encode(self)
    }
    
    func jsonString(encoding: String.Encoding = .utf8) throws -> String? {
        return String(data: try self.jsonData(), encoding: encoding)
    }
}

